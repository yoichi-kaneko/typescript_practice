// any型の学習
let anyValue: any = "Hello, World!"; // any型はどんな値も許容する
anyValue = 100;
anyValue = true;
anyValue = { name: "Alice" };
anyValue = [1, 2, 3];
anyValue = null; // any型はnullも許容する
anyValue = undefined; // any型はundefinedも許容する
anyValue = () => "This is a function"; // any型は関数も許容する
anyValue = new Date(); // any型はDateオブジェクトも許容する


